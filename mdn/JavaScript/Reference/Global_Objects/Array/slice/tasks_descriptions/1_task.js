/**
 * #Task 1: Примените метод slice что бы получить следующие значения
 */

const animals = ['ant', 'bison', 'camel', 'duck', 'elephant'];

animals.slice ;// ["camel", "duck", "elephant"]
animals.slice ;// ["camel", "duck"]
animals.slice ;// ["bison", "camel", "duck", "elephant"]
animals.slice ;// ["duck", "elephant"]
animals.slice ;// ["camel", "duck"]
animals.slice //["ant", "bison", "camel", "duck", "elephant"]

